public class wiseSaying {
    private int num;
    private String wise;
    private String author;

    public wiseSaying(int num, String wise, String author) {
        this.num = num;
        this.wise = wise;
        this.author = author;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }

    public String getWise() {
        return wise;
    }

    public void setWise(String wise) {
        this.wise = wise;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }
}
